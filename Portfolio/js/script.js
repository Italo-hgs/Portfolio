function abrirMenu(){
    var x = document.getElementById('open');
    if(x.style.display === 'block'){
        x.style.display = 'none';
    }else{
        x.style.display = 'block';
    }
}

function fecharMenu(){
    var a = document.getElementById('open');
    if(a.style.display === 'block'){
        a.style.display = 'none';
    }else{
        a.style.display = 'block';
    }
}